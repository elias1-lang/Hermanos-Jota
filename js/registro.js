//Actividad clase: solicitar nombre, apellido y un correo usando variables, prompt(), console.log(), alert()

const nombreCliente = prompt("Ingrese su nombre "); 
const apellidoCliente = prompt("Ingrese su apellido "); 
const emailCliente = prompt("Ingrese su email "); 
alert(`Cliente Nombre: ${nombreCliente}. Cliente Apellido:${apellidoCliente}. Cliente Email ${emailCliente}` );
console.log("El usuario logeado tiene el nombre: ", nombreCliente);
console.log("El usuario logeado tiene el apellido: ", apellidoCliente);
console.log("El usuario logeado tiene el email: ", emailCliente);